</span></span></span><![endif]><span style='font-size:16.0pt;line-height:107%;
color:#9933FF'>Annual Managers Meeting / Rule Changes<o:p></o:p></span></h2>

<p class=MsoNormal>The league will accept suggestions for rule changes for the
upcoming season(s) from the culmination of the league championship (end of NFL
week 17) until the NFL Draft each season. The commissioner solely holds the
power to bring a rule change to a vote outside of the aforementioned timeframe.</p>

<p class=MsoNormal>Any manager can put forward a suggestion of a rule change,
though the commissioner holds sole power to withhold a change from being taken
to a vote. This will be used only in extreme cases (basically anything thats
not totally crazy or league-ruining or against the ethos of the league will be
sent to a vote). The majority required for a rule change to pass will be set
individually for each proposal. Most of the time, it will be a simple over
50% majority required, but for more wide-ranging or serious changes, it may be
67%, 75%, or unanimous.</p>

<p class=MsoNormal>Suggested rule changes can include scoring adjustments,
waiver wire rules, league calendar adjustments - anything that is set in
Sleeper rules or in the bylaws is up for debate each and every year.</p>

<p class=MsoNormal>Unless otherwise agreed, scoring and starting-roster changes
will be implemented a minimum of two years after approval. This allows managers
time to plan forward with their transactions for the change.</p>

<p class=MsoNormal>In the event of <b style='mso-bidi-font-weight:normal'>any</b>
league-wide vote, co-managers of the same team receive only one vote as a team.
Only teams up-to-date on dues will be allowed to vote.</p>

