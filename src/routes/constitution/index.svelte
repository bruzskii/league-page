style='font:7.0pt "Times New Roman"'>&nbsp; </span></span></span><![endif]><span
style='color:#9933FF'>Roster Size<o:p></o:p></span></h3>

<p class=MsoNormal>Active Rosters are a maximum of 30 players consisting of 21
bench spots and 9 starters. Starting lineups consist of 1 QB, 2 RBs, 2 WRs, 1
TE, 2 Flex (RB/WR/TE) and 1 Superflex (QB/RB/WR/TE).</p>

<p class=MsoNormal>No IR spots are made available.</p>

<p class=MsoNormal>During the off-season, Sleeper will allow newly-drafted
rookies to be placed on active rosters without cutting players. While this is
allowed, players are bound by platform rules regarding these overages.
Transactions may be restricted by the platform until they are within compliance
of platform rules.</p>

<h3 style='margin-left:.25in;text-indent:-.25in;mso-list:l4 level2 lfo1'><a
name="_heading=h.1fob9te"></a><![if !supportLists]><span style='mso-fareast-font-family:
