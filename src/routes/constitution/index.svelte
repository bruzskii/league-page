style='color:#9933FF'>Veto Powers<o:p></o:p></span></h3>

<p class=MsoNormal>The commissioner will have the final decision on whether a
trade (especially involving future picks) is truly damaging to the future of
the league, but this power will only be used in absolute extreme circumstances,
when there are clear signs that a manager is underselling picks (or players)
with malicious intent. Its unlikely that any trade will ever be rejected for
this reason. To be clear, trading of picks because youre in win-now mode doesnt
count as malicious; this commissioner ability is reserved for flagrant abuse of
the league.</p>

<p class=MsoNormal>Trades cannot be vetoed unless the trade is an obvious
attempt at collusion. The commissioner will bring these to a league vote, where
a super-majority (2/3<sup>rds</sup>) of the remaining league is needed to veto
the trade. Assuming a trade is between two individuals (leaving 10 remaining
members) 7 votes to veto required. If this power is invoked, members are
REQUIRED to vote  abstaining will be considered a vote against vetoing. </p>

<h3 style='margin-left:.25in;text-indent:-.25in;mso-list:l2 level2 lfo5'><a
