style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span></span><![endif]><span style='color:#9933FF'>Conclusion<o:p></o:p></span></h3>

<p class=MsoNormal>The aim of the Leaders vs Legends Dynasty league is to be a
fun, friendly, active NFL dynasty league thats enjoyable for everyone
involved. Try to remain as active as you can, especially during the offseason:
one of the best things about dynasty leagues is that they keep you entertained
during the long, game-starved summer! In addition, Dynasty Fantasy Football is
great because <i style='mso-bidi-font-style:normal'>even when you are losing,
you are winning!<o:p></o:p></i></p>

<p class=MsoNormal>Managers are encouraged to take part in league chat using
the league platform (Sleeper), and expected to respond to league polls and take
part in discussions regarding rule changes and other league matters.</p>

<p class=MsoNormal>Finally, managers must show willingness to engage in the
league and an interest in leading their team to incredible glory or abject failure
over the long-term. Non-active owners will be removed from the league and
replaced if attempts to resolve the situation with the owner fail to be
successful, as this is necessary to keep the league enjoyable for all owners.</p>

