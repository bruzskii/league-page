keep the rookie draft on time. The commissioner will not be required to remind
members to pay dues.</p>

<p class=MsoNormal>No league dues will be refunded except under extreme
conditions, including dues to the rolling pot. Members leaving the league, who
have paid ahead, forfeit their dues as prepaid league dues to their successors.</p>

