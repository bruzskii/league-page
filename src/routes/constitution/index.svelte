mso-bidi-theme-font:major-latin;color:#9933FF'><span style='mso-list:Ignore'>6.3<span
style='font:7.0pt "Times New Roman"'>&nbsp; </span></span></span><![endif]><span
style='color:#9933FF'>Pick Trading Stipulation<o:p></o:p></span></h3>

<p class=MsoNormal>For the first two years of membership in the league, members
must pay league dues ahead if they trade away a 1<sup>st</sup> round draft
pick. This is in an effort to hedge against individuals unsuccessfully going
all-in, and then abandoning a broken team. Beginning in year 3 of an
individual being in the league, there is no requirement to pay forward.</p>

<p class=MsoNormal>If, at any time, section 6.2 regarding veto powers is
invoked, this rule is immediately reinitiated for 2 years across the league and
anyone without first round picks in future years immediately has their league
dues come due for each year without a first-round pick. This can only be
overturned by a supermajority (2/3<sup>rds</sup>) vote. Assuming the league is
at 12, that requires 8 votes.</p>

