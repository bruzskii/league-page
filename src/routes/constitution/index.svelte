</span></span></span><![endif]><span style='font-size:16.0pt;line-height:107%;
color:#9933FF'>Ethos<o:p></o:p></span></h2>

<h3 style='margin-left:.25in;text-indent:-.25in;mso-list:l2 level2 lfo5'><a
name="_heading=h.49x2ik5"></a><![if !supportLists]><span style='mso-fareast-font-family:
"Calibri Light";mso-fareast-theme-font:major-latin;mso-bidi-font-family:"Calibri Light";
mso-bidi-theme-font:major-latin;color:#9933FF'><span style='mso-list:Ignore'>10.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span></span><![endif]><span style='color:#9933FF'>Tanking/Abandonment<o:p></o:p></span></h3>

<p class=MsoNormal>Managers must make an effort to keep their team competitive
at all points of the season. Tanking - making a purposeful effort to lose a
game with the intention of gaining a higher draft pick - is outlawed. An
example of tanking is benching a probable high-scoring player in favor of a
probable low-scoring player. Another example would be not playing a full
starting lineup. Should we develop a close-knit, communicative league, this is
unlikely to be an issue. In addition, it is incredibly difficult to prove. That
being said, any owner deemed to be tanking will be warned of the possibility of
being replaced, or immediately replaced, depending on the seriousness of the
offence. On deciding what is deemed tanking, the commissioners decision is
final. The commissioner also holds discretion to privately poll the other
members of the league in an effort to make a determination.</p>

<p class=MsoNormal>Should a manager abandon their team, and play an
incomplete starting lineup, or a lineup deemed as tanking, the commissioner
will set their lineup each week in accordance with industry rankings. If it is
clear an abandonment has slowly occurred (i.e., 1 injured player left in lineup
week 3, then 2 in week 4, etc.), the commissioner may retroactively set lineups
according to pre-week industry rankings, and recalculate standings. In the case
of retroactive changes, this should only include players that did not play due
to injury or byes. The commissioner should not go back further than 3 weeks to
make corrections. If a commissioner is forced to set a players lineup, the
commissioner will take control of that team for the remainder of the year, or
until a replacement manager is identified. A temporary manager may be assigned
to the team to make educated start/sit decisions. No roster changes will be
made for the team by the commissioner or a temporary manager  and claims on
taxi squad players will be locked. In this case, an immediate ban on the
offending manager, as well as replacement proceedings are expected to be
initiated so the commissioner can relinquish power over the team as soon as
possible. There may be extenuating circumstances where the commissioner chooses
not to pursue takeover and replacement proceedings, or where the commissioner
puts these proceedings in the hands of a league vote. The commissioner will
decide, based on the severity of the case, the number of votes needed for
immediate removal or ban from the league. As noted in section 8.1, no league
dues will be refunded except under extreme conditions, including dues to the
rolling pot.</p>

<h3 style='margin-left:.25in;text-indent:-.25in;mso-list:l2 level2 lfo5'><a
name="_heading=h.2p2csry"></a><![if !supportLists]><span style='mso-fareast-font-family:
"Calibri Light";mso-fareast-theme-font:major-latin;mso-bidi-font-family:"Calibri Light";
mso-bidi-theme-font:major-latin;color:#9933FF'><span style='mso-list:Ignore'>10.2<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span></span><![endif]><span style='color:#9933FF'>Retirement<o:p></o:p></span></h3>

<p class=MsoNormal>If you as a manager decide to retire from the league, please
let the commissioner know as soon as possible; even if you decide in the middle
of the year that you will be retiring at the end of the year. This will aid in
backfilling managers, and possibly even lead to a manager being able to take
over mid-season, without an interruption in the league schedule. Members
leaving the league, who have paid ahead, forfeit their dues as prepaid league
dues to their successors. Successors can choose to refund the retiring manager
separately if the team is left in good standing. This will not be a
responsibility of the league or the commissioner to determine. A retiring
manager may request a specific heir to their team from the waitlist. It is at
the commissioners discretion to approve or reject this request. Leaving a team
and the league in good standing will significantly increase the chances of an
heir being approved.</p>

