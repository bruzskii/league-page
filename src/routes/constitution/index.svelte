mso-bidi-theme-font:major-latin;color:#9933FF'><span style='mso-list:Ignore'>9.1<span
style='font:7.0pt "Times New Roman"'>&nbsp; </span></span></span><![endif]><span
style='color:#9933FF'>Collect and Manage League Dues<o:p></o:p></span></h3>

<p class=MsoNormal>The commissioner, first and foremost, is responsible for
collection and tracking of league dues. Any member of the league can request
the commissioner to provide a bank statement with proof-of-funds or
proof-of-ability to pay league winnings in full. If they cannot provide this,
they will be stripped of their commissioner title, and potentially banned from
the league pending a league vote (simple majority  50%).</p>

<h3 style='margin-left:.25in;text-indent:-.25in;mso-list:l2 level2 lfo5'><a
name="_heading=h.qsh70q"></a><![if !supportLists]><span style='mso-fareast-font-family:
"Calibri Light";mso-fareast-theme-font:major-latin;mso-bidi-font-family:"Calibri Light";
mso-bidi-theme-font:major-latin;color:#9933FF'><span style='mso-list:Ignore'>9.2<span
style='font:7.0pt "Times New Roman"'>&nbsp; </span></span></span><![endif]><span
style='color:#9933FF'>League <span class=SpellE>WaitList</span><o:p></o:p></span></h3>

<p class=MsoNormal>The commissioner shall maintain a waiting list for entry
into the league. Individuals on the waiting list will be invited upon an
opening, or a league-approved expansion of the league (requiring a minimum of a
75% vote - or larger as chosen by the commissioner). If an individual declines
their invitation to the league, they can remain on the waiting list, but are
moved to the bottom. If multiple teams are available, multiple invitations
should be sent simultaneously. The individual to first accept will have their
first choice of team. In the case where multiple openings occur in the same
offseason, but one spot has been filled before the other is open, the invitee
will not have the choice to switch to the newly open team. </p>

