mso-bidi-theme-font:major-latin;color:#9933FF'><span style='mso-list:Ignore'>1.2<span
style='font:7.0pt "Times New Roman"'>&nbsp; </span></span></span><![endif]><span
style='color:#9933FF'>Retired Players<o:p></o:p></span></h3>

<p class=MsoNormal>Should a player retire and remain active on the platform
(see Andrew Luck 2019-2020), that player is treated just like any active
player. This player will count against the teams roster space until
dropped/traded.</p>

<p class=MsoNormal style='border:none;mso-padding-alt:31.0pt 31.0pt 31.0pt 31.0pt;
mso-border-shadow:yes'><span style='color:black'>If a retired player is removed
from the platform while on an active roster, the manager with that player may
place a petition to the commissioner to have that player reinstated as a member
of their team should they ever unretire. The petition needs to be placed, and
approved, prior to the manager filling the newly-opened spot on their team.<o:p></o:p></span></p>

<h3 style='margin-left:.25in;text-indent:-.25in;mso-list:l4 level2 lfo1'><a
name="_heading=h.3znysh7"></a><![if !supportLists]><span style='mso-fareast-font-family:
"Calibri Light";mso-fareast-theme-font:major-latin;mso-bidi-font-family:"Calibri Light";
mso-bidi-theme-font:major-latin;color:#9933FF'><span style='mso-list:Ignore'>1.3<span
style='font:7.0pt "Times New Roman"'>&nbsp; </span></span></span><![endif]><span
style='color:#9933FF'>Taxi Squads<o:p></o:p></span></h3>

<p class=MsoNormal>Each team is allotted 6 taxi squad spots (as voted on
3/1/21). These spots provide teams with an option to stash rookies or younger
players into additional slots that do not count against a team's roster limit.
These players cannot be used in your starting lineup unless elevated to the
active roster.</p>

<p class=MsoNormal>To designate a player to the taxi squad, they must be
demoted to the taxi squad on Sleeper prior to the designation deadline, which
is currently the start of the regular season. Players cannot be added to taxi
squads during the season unless platform rules otherwise allow.</p>

<p class=MsoNormal>Rookies and 2<sup>nd</sup> year NFL players are taxi squad
eligible. If a player is promoted during their 1st season, they are allowed,
per platform rules, to be placed back on the taxi squad the following
offseason. Draft compensation for these players does not change upon a claim. <b
style='mso-bidi-font-weight:normal'><span style='color:red'>YEAR 1 IS THE
EXCEPTION TO THIS RULE - ONLY ROOKIES WILL BE ALLOWED ON THE TAXI SQUAD IN THE
2021 FANTASY SEASON.<o:p></o:p></span></b></p>

<p class=MsoNormal>Rookies and 2<sup>nd</sup> year players acquired in the
preseason via waivers are taxi-squad eligible in accordance to platform rules.</p>

<p class=MsoNormal>Players can be promoted to the active roster at any time.
Once a player is on a teams active roster, they cannot be demoted back to the
taxi squad until the following offseason.</p>

<p class=MsoNormal>Taxi squad players are allowed to be traded off the taxi
squad, but they must be placed on the active roster of the receiving team.
Sleeper support notes that the sending team should not have to elevate the
player to the active roster prior to being traded. However, if the platform
ends up requiring this, and thus forcing a drop of another player prior to the
trade being made, the sending team will receive the dropped player back after
the trade via commissioner action without that player being subject to waivers.
However, this must be declared prior to the trade being approved and finalized
by the commissioner.</p>

